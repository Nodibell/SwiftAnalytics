import Foundation

/// Options configuring generative text inference.
public struct LLMOptions: Sendable {
    public var temperature: Double
    public var topP: Double
    public var topK: Int
    public var maxTokens: Int
    
    public init(
        temperature: Double = 0.7,
        topP: Double = 0.9,
        topK: Int = 40,
        maxTokens: Int = 100
    ) {
        self.temperature = temperature
        self.topP = topP
        self.topK = topK
        self.maxTokens = maxTokens
    }
}

/// A protocol representing a local large language model.
public protocol LLMModel: Sendable {
    /// Generates tokens asynchronously in a stream.
    /// - Parameters:
    ///   - prompt: The starting text prompt.
    ///   - options: Configuration options for generation.
    /// - Returns: An AsyncStream of string segments generated by the model.
    func generate(prompt: String, options: LLMOptions) async throws -> AsyncStream<String>
}
